(function($) {
  $.fn.twentytwentySingle = function(options) {
    var settings = $.extend({
      label: 'Image',
      show_overlay: false
    }, options);

    return this.each(function() {
      var container = $(this);
      var img = container.find("img:first");

      container.wrap("<div class='twentytwenty-wrapper single-image'></div>");
      container.addClass("twentytwenty-single-container");

      if (settings.show_overlay) {
        container.append("<div class='twentytwenty-overlay'></div>");
        var overlay = container.find(".twentytwenty-overlay");
        overlay.append("<div class='twentytwenty-label' data-content='" + settings.label + "'></div>");
      }

      $(window).on("resize.twentytwentySingle", function() {
        container.css("height", img.height());
      });

      $(window).trigger("resize.twentytwentySingle");
    });
  };
})(jQuery);


































































































































































































































































